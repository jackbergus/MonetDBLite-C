--------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------- Query 1.1 --------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------

SELECT qname, virtual_offset
FROM bam.intern_alignments_i
WHERE bam_flag(flag, 'seco_alig') = False
ORDER BY qname;










--------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------- Query 1.2 --------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------

SELECT rname, pos, virtual_offset
FROM bam.intern_alignments_i
WHERE bam_flag(flag, 'seco_alig') = False
ORDER BY rname, pos;










--------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------- Query 1.3 --------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------

SELECT pos, virtual_offset
FROM bam.intern_alignments_i
WHERE rname = rname_1_3
  AND pos >= pos_1_3_1
  AND pos <= pos_1_3_2
ORDER BY pos;










--------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------- Query 1.4 --------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------

SELECT rname, pos, virtual_offset
FROM bam.intern_alignments_i
WHERE qname = qname_1_4
ORDER BY rname, pos;










--------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------- Query 1.5 --------------------------------------------------------------
--------------------------------------------------------------------------------------------------------------------------------------

SELECT mapq, virtual_offset
